    private void btnClick_A(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnClick_A
        switch(selectedAnswer){
            case 'a':
                paintButton(btnAnswerA, ItemEvent.DESELECTED);
                selectedAnswer = 'n';
                break;
            case 'b':
                paintButton(btnAnswerB, ItemEvent.DESELECTED);
                paintButton(btnAnswerA, ItemEvent.SELECTED);
                selectedAnswer = 'a';
                break;
            case 'c':
                paintButton(btnAnswerC, ItemEvent.DESELECTED);
                paintButton(btnAnswerA, ItemEvent.SELECTED);
                selectedAnswer = 'a';
                break;
            case 'd':
                paintButton(btnAnswerD, ItemEvent.DESELECTED);
                paintButton(btnAnswerA, ItemEvent.SELECTED);
                selectedAnswer = 'a';
                break;
            default:
                paintButton(btnAnswerA, ItemEvent.SELECTED);
                selectedAnswer = 'a';
        }        
    }//GEN-LAST:event_btnClick_A

