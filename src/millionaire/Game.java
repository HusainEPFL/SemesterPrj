    /** Paints the given Button based on the selection event. 
     * 
     * @param btn  The button of which state is changed
     * @param itmEvent  Shows whether the passed ToggleButton selected or deselected
     * 
     */
    private void paintButton(JButton btn, int itmEvent) {      
        if(itmEvent == ItemEvent.SELECTED){
            btn.setBackground(Color.yellow);
            btn.setForeground(Color.RED);
        }
        else{            
            float[] hsbcolor = Color.RGBtoHSB(0, 51, 204,null);
            btn.setBackground(Color.getHSBColor(hsbcolor[0], hsbcolor[1], hsbcolor[2]));
            btn.setForeground(Color.WHITE);
        }     
    }
    
